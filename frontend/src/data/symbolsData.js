// Get all symbols
export const getAllSymbols = () => {
  return [
    ...symbolsData.basic,
    ...symbolsData.special,
    ...symbolsData.comparison,
    ...symbolsData.fractions
  ];
};

// Get symbols by category
export const getSymbolsByCategory = (category) => {
  return symbolsData[category] || [];
};

// Get random symbol
export const getRandomSymbol = () => {
  const allSymbols = getAllSymbols();
  return allSymbols[Math.floor(Math.random() * allSymbols.length)];
};

